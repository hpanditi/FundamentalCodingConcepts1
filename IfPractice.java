public class IfPractice {
    public static void main(String[] args) {

        int myIntegerOne = 40;

        if(myIntegerOne < 25) {
            System.out.println("Yes, it's true!");
        }
        else if (myIntegerOne > 50) {
            System.out.println("No, it's false!");
        }
        else {
            System.out.println("None of the above");
        }
    }
}

