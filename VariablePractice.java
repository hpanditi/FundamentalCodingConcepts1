public class VariablePractice {
    public static void main(String[] args) {
        int myFirstOne;
        myFirstOne = 1;
        short myShort = 8;
        long myLong = 1000;
        double myDouble = 1.2345;
        float myFloat = 123.4f;
        char myChar = 'y';
        boolean myBooleanOne = true;
        byte myByteOne = 123;

        System.out.print("My first variable 'myFirstOne' is: ");
        System.out.println(myFirstOne);

        System.out.print("The variable short 'myShort' is: ");
        System.out.println(myShort);

        System.out.print("The variable long 'myLong' is: ");
        System.out.println(myLong);

        System.out.print("The variable double 'myDouble' is: ");
        System.out.println(myDouble);

        System.out.print("The variable float 'myFloat' is: ");
        System.out.println(myFloat);

        System.out.print("The variable char 'myChar' is: ");
        System.out.println(myChar);

        System.out.print("The variable boolean 'myBooleanOne' is: ");
        System.out.println(myBooleanOne);

        System.out.print("The variable byte 'myByteOne' is: ");
        System.out.println(myByteOne);
    }
}
